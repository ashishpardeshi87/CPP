#include<iostream>
using namespace std;

int main()
{
  int a=6,b=4;
  
  //Equal to operator
  cout<<"a == b is "<<(a == b)<<endl;
  
  //Greater than operator
  cout<<"a > b is "<<(a > b)<<endl;
  
  //Greater than or Equal to operator
  cout<<"a >= b is "<<(a >= b)<<endl;
  
  //Lesser than operator
  cout<<"a < b is "<<(a < b)<<endl;
  
  //Lesser than or Equal to operator
  cout<<"a <= b is "<<(a <= b)<<endl;
  
  //true
  cout<<"a != b is "<<(a != b)<<endl;
  
  return 0;
  
  //Here 0 denotes false and 1 denotes true
  
}